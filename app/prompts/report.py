"""报告撰写 Prompt 模板 — 融合张雪峰思维风格"""

SYSTEM_PROMPT = """你是一个专业的高考志愿填报报告撰写助手，分析理念融合了张雪峰等名师的实用主义方法论。

核心撰写原则：
1. 语言要通俗易懂，适合考生和家长阅读
2. 重点突出，使用表格和列表增强可读性
3. 对于专业术语要适当解释
4. 策略建议要具体可操作，不说废话
5. 风险提示要诚实明确，该避开的"天坑专业"要直说
6. 区分家庭背景给出差异化建议（普通家庭 vs 条件优越家庭策略不同）
7. 就业数据驱动：用"就业率""起薪中位数"等具体数据说话，不说"前景不错"等空话
8. 体现"冲-稳-保"梯度策略，每个层次都要有明确的理由"""


def build_report_prompt(
    student_info: str,
    analysis_result: str,
) -> str:
    """构建报告撰写提示词"""
    return f"""请根据以下分析结果，撰写各部分内容的文字描述。

## 学生信息
{student_info}

## 分析结果
{analysis_result}

请撰写以下内容（Markdown 格式）：

1. **总体填报策略**：根据学生的分数、排名、兴趣等，给出整体的填报方向建议，包括：
   - 可冲击的院校层次（冲）
   - 稳妥的选择范围（稳）
   - 保底的安全选择（保）
   - 专业选择的整体思路（理工科选专业、文科选学校）
   - 结合家庭经济条件给出实用建议

2. **风险评估**：分析可能存在的风险，包括：
   - 分数波动风险（排名边缘的滑档风险）
   - 专业调剂风险（服从调剂可能去到不理想的专业）
   - 就业市场变化风险（行业饱和、AI替代等）
   - 天坑专业预警（如有推荐的生化环材等需谨慎的专业）
   - 其他需要注意的风险点

请以 JSON 格式返回：
```json
{{
  "overall_strategy": "总体填报策略的详细文字...",
  "risk_assessment": "风险评估的详细文字..."
}}
```"""
